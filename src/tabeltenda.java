    public void loadDatatenda(){
        Kode_tenda = txttenda.getText();
        Nama_tenda = txtNama.getText();
        Jenis_tenda = (String) txtjenis.getSelectedItem();
        Kapasitas = (String) txtkapas.getSelectedItem();
        Harga = txtharga.getText();
    }
    
    //memilih data saat baris pada tabel dipilih dengan mouse
    public void dataSelect(){
        //deklarasi variabel
        int i = tabeltenda.getSelectedRow();
        
        //uji adakah data di tabel?
        if(i == -1){
            //tidak ada yang terpilih atau dipilih.
            return;
        }
        txttenda.setText(""+model.getValueAt(i,0));
        txtNama.setText(""+model.getValueAt(i,1));
        txtjenis.setSelectedItem(""+model.getValueAt(i,2));
        txtkapas.setSelectedItem(""+model.getValueAt(i,3));
        txtharga.setText(""+model.getValueAt(i,4));
    }
     
